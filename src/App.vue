<template>
  <h1>Reaction Timer</h1>
  <button @click="start" :disabled="isPlaying">play</button>
  <Block v-if="isPlaying" :delay="delay" @end="endGame" /> 
  <Results v-if="showResults" :score="score"/>
</template>

<script>
import Block from './components/Block.vue'
import Results from './components/Results.vue'

export default {
  name: 'App',
  components: { Block, Results },
  data() {
    return {
      isPlaying: false,
      delay: null,
      score: null,
      showResults: false
    }
  },
  methods: {
    start() {
      this.delay = 2000 + Math.random() * 5000 //get a random delay between 2000 and 7000ms
      this.isPlaying = true
      this.showResults = false
      //console.log(this.delay)
    },
    endGame(reactionTime) {
      this.score = reactionTime //set reaction time as score
      this.isPlaying =  false //set game play state to false
      this.showResults = true //show score
    }
  }
}
</script>

